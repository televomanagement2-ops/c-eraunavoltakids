/** URL Unsplash stabile (solo slug `photo-*`). Parametri Imgix allineati al CDN ufficiale. */
export function u(slug: string) {
  const params = new URLSearchParams({
    ixlib: 'rb-4.0.3',
    auto: 'format',
    fit: 'crop',
    w: '680',
    q: '82',
  })
  return `https://images.unsplash.com/${slug}?${params.toString()}`
}

